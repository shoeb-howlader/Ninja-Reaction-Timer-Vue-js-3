<template>
    <div>
<p >Reaction Time : {{score}}</p>
<p class="rank">{{rank}}</p>
    </div>
</template>

<script>
    export default {
        props: ['score'],
        data() {
            return {
                rank: null
            }
        },
        mounted() {
            if(this.score<250)
            {
                this.rank='ninja fingers'
            }
            else if (this.score<400)
            {
                this.rank='Rapid refelxes'
            }
            else
            {
                this.rank='Snail pace....'
            }
        },
    }
</script>

